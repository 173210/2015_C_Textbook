#include <cstdio>
#include <iostream>
#include <cstring>
#include "strlen.h"
void strstr_and_do(const char* str, auto (*strsearch)(const char*) -> size_t){
	const auto re = strsearch(str);
	if (0 == re){
		puts("エラーなんだぜ");
	}
	else{
		std::cout << re << "文字だよ" << std::endl;
		printf("%d文字だよ\n", re);
	}
}
int main(void){
	auto str = "arikitari_na_world!";

	printf("base_str : %s\n", str);

	puts("string.hのstrstr関数を呼び出します");
	strstr_and_do(str, strlen);
	puts("strstr.hのmy_strstr関数を呼び出します");
	strstr_and_do(str, my_strlen);
	return 0;
}
